/**
 * @author Tommaso
 * Class that declares aithmetic operation methods
 */
public class ArithmeticOperators {
	/**
	 * Method to sum 2 given int
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int sum(int num1, int num2){
		return num1+num2;
	}
	/**
	 * Method to subtract 2 given int
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int sub(int num1, int num2){
		return num1-num2;
	}
	/**
	 * Method to multiply 2 given int
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int mul(int num1, int num2){
		return num1*num2;
	}
	/**
	 * Method to divide 2 given int
	 * @param num1
	 * @param num2
	 * @return
	 */
	public int div(int num1, int num2){
		return num1/num2;
	}

	/**
	 * constructor to instantiate the object
	 */
	public ArithmeticOperators(){}

}
